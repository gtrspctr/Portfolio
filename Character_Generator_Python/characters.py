class Character:
    def __init__(self,
                 race,
                 img,
                 max_hp,
                 max_mp,
                 p_atk,
                 p_def,
                 m_atk,
                 m_def,
                 spd):
        self.race = race
        self.img = img
        self.max_hp = max_hp
        self.max_mp = max_mp
        self.p_atk = p_atk
        self.p_def = p_def
        self.m_atk = m_atk
        self.m_def = m_def
        self.spd = spd
